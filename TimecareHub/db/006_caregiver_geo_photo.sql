-- ---------- รูปโปรไฟล์ ----------
-- เก็บที่ users เพราะเป็นรูป "ของคน" ไม่ใช่ "ของบทบาท"
-- (1 บัญชีสลับ 2 บทบาท → ใช้รูปเดียวกันทั้งตอนจ้างคนและตอนรับงาน)
--
-- เก็บแค่ path — ตัวไฟล์อยู่ใน uploads/avatars/ ซึ่งอยู่ "นอก" public/ โดยตั้งใจ
-- กติกาเดียวกับรูปในแชท: เข้า URL ตรงไม่ได้ ต้องผ่าน route ที่เช็คล็อกอินก่อน
ALTER TABLE users ADD COLUMN photo_path VARCHAR(255) NULL;

-- ---------- รัศมีที่แคร์กิฟเวอร์ยอมเดินทางไปทำงาน ----------
-- lat/lng ของแคร์กิฟเวอร์มีมาตั้งแต่รอบ 002 แล้ว แต่ไม่เคยมีหน้าจอให้ปักจริง
-- (เข้าได้ทางเดียวคือ db/seed.js) — รอบนี้ปักเองได้จากบัตรแคร์กิฟเวอร์
ALTER TABLE caregiver_profiles ADD COLUMN service_radius_km INT NOT NULL DEFAULT 10;

-- ค้นหาตามรัศมีต้องกวาด lat/lng ทุกแถว — ใส่ index ไว้เหมือนที่ jobs มี (idx_geo)
ALTER TABLE caregiver_profiles ADD INDEX idx_cg_geo (lat, lng);
