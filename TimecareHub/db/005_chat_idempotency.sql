--  รอบที่ 5 — กันข้อความซ้ำตอนกดส่งใหม่
--
--  ปมคือ: หน้าเว็บรอ ack 12 วิ ไม่มาก็ขึ้นเครื่องหมายตกใจให้แตะส่งซ้ำ
--         แต่ "ack ไม่มา" ไม่ได้แปลว่า "server ไม่ได้บันทึก" — สายอาจหลุดตอน ack กำลังเดินทางกลับ
--         กดส่งซ้ำตอนนั้น = ข้อความเดียวกันลง DB 2 แถว คนรับเห็นเด้ง 2 ที
--
--  แก้โดยจำ client_id (เลขอ้างอิงที่หน้าเว็บสร้างเองตอนกดส่ง) ไว้ แล้วกันซ้ำที่ระดับ DB
--  ส่งซ้ำด้วย client_id เดิม → ชน unique → คืนแถวเดิมกลับไป ไม่บันทึกใหม่
--  (เช็คในโค้ดแทนไม่ได้: SELECT แล้วค่อย INSERT มีช่องว่างให้ 2 request แทรกพร้อมกันเสมอ)
--
--  รันซ้ำได้ — migrate.js จะข้าม error "คอลัมน์มีอยู่แล้ว" ให้เอง
-- ============================================================

-- VARCHAR(32) พอสำหรับรูปแบบที่หน้าเว็บสร้าง (c + เวลา 13 หลัก + สุ่ม 6 ตัว = 20 ตัว)
-- ฝั่ง server ตัดให้พอดี 32 ก่อน INSERT อยู่แล้ว (chat-core.js) — ค่าประหลาดยาว ๆ ทำพังไม่ได้
ALTER TABLE messages ADD COLUMN client_id VARCHAR(32) NULL AFTER receiver_id;

-- คู่ (คนส่ง, client_id) ต้องไม่ซ้ำ — ต้องมี sender_id ด้วย ไม่งั้น 2 คนสุ่มเลขชนกันแล้วข้อความหาย
--
-- ⚠️ ใน MySQL ค่า NULL ไม่นับว่าซ้ำกันเอง → ข้อความเก่าที่ client_id เป็น NULL ทั้งคอลัมน์
--    อยู่ร่วมกันได้สบาย ดัชนีนี้ไม่กระทบข้อมูลเดิมเลย
ALTER TABLE messages ADD UNIQUE INDEX idx_client (sender_id, client_id);
